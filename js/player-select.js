document.getElementById('select-1').addEventListener('click', function(){
    const playerName = document.getElementById('player-1');
    const liParent = document.getElementById('selected-player-container')
    const li = document.createElement('li');
    li.innerText = playerName.innerText;
    liParent.appendChild(li)
})
